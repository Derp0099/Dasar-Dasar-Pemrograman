import math
# Rumus persegi
def persegi(sisi):
    return pow(sisi,2)
# Rumus luas dan keliling persegi panjang
def lpp(p,l):
    return p * l
# Rumus segitiga
def segitiga(a,t):
    return a * t * 1/2
# Rumus lingkaran
def lingkaran(r):
    hasil = 22/7 * pow(r,2)
    return hasil
# Rumus jajar genjang
def jg(a,t):
    return a * t